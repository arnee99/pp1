#include <iostream>  
using namespace std;  
  
int main() {  
     int a = 5;
     bool b = true; 
     bool c = false;
     // !false = true  
     // b == true, !b -> false
     cout << !b << endl;  
  
     // !true = false  
     // c == false, !c -> true
     cout << !(c) << endl;  
  
     return 0;  
}  