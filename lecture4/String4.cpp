#include <iostream>
#include <string> // for string class
using namespace std;
  
// Driver Code
int main()
{
    // Declaring string
    string str;
  
    // Taking string input using getline()
    getline(cin, str);
  
    // Displaying string
    cout << "The initial string is : ";
    cout << str << endl;
  
    // Inserting a character
    str.push_back('A');
  
    // Displaying string
    cout << "The string after push_back operation is : ";
    cout << str << endl;
  
    // Deleting a character
    str.pop_back();
  
    // Displaying string
    cout << "The string after pop_back operation is : ";
    cout << str << endl;
  
    return 0;
}